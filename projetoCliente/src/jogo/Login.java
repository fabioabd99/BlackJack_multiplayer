    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:

        if (!"".equals(ipCliente.getText()) && !"".equals(portoCliente.getText()) && !"".equals(usernameCliente.getText())) {

            try {

                ip = ipCliente.getText();
                username = usernameCliente.getText();
                porto = Integer.valueOf(portoCliente.getText());

                GerirInterface session = (GerirInterface) LocateRegistry.getRegistry(ip, porto).lookup("blackjack");
                CallBackInterface callback = new CallBack();

                for (int i = 0; i < session.getJogadores().length; i++) {
                    if (session.getJogadores()[i] != null) {
                        if (session.getJogadores()[i].getUsername().equals(username)) {
                            System.out.println("nome ja existe");
                            JOptionPane.showMessageDialog(this,"O usernane ja existe.");
                            return;
                        }
                    }
                }

                JogadorInterface player = session.login(callback, username);

                if (player == null) {
                    System.out.println("NÃ£o ha lugar na mesa");
                    return;
                }

                jButton4.setEnabled(false);
                MainFrame mf = new MainFrame(player, session);
                ((CallBack) callback).setMainFrame(mf);
                mf.setVisible(true);

            } catch (RemoteException ex) {
                Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
            } catch (NotBoundException ex) {
                Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            System.out.println("Preencha todos os campos");
        }

    }//GEN-LAST:event_jButton4ActionPerformed

